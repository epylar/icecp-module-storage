package com.intel.icecp.module.query;

/**
 * Represent a tag that groups messages in the database
 *
 */
public final class Tag implements Query.Identifier<String> {
    private final String value;

    public Tag(String tag) {
        this.value = tag;
    }

    @Override
    public String value() {
        return value;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) {
            return true;
        }
        if (o == null || getClass() != o.getClass()) {
            return false;
        }
        Tag tag = (Tag) o;
        return value != null ? value.equals(tag.value) : tag.value == null;
    }

    @Override
    public int hashCode() {
        return value != null ? value.hashCode() : 0;
    }

    @Override
    public String toString() {
        return "Tag{" + value + '}';
    }
}